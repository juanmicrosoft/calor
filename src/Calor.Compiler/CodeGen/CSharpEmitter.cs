        if (node.ElseBody != null && node.ElseBody.Count > 0)
        {
            AppendLine("#else");
            foreach (var stmt in node.ElseBody)
            {
                var code = stmt.Accept(this);
                if (!string.IsNullOrEmpty(code)) AppendLine(code);
            }
        }
        AppendLine("#endif");
        return "";
    }

