    public string Visit(PreprocessorDirectiveNode node)
    {
        AppendLine($"#if {node.Condition}");
        foreach (var stmt in node.Body)
        {
            var code = stmt.Accept(this);
            if (!string.IsNullOrEmpty(code)) AppendLine(code);
